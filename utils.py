def drawBox(img,x1,y1,x2,y2,label):
    img = cv2.rectangle(img, (x1, y1), (x2, y2), (0, 255, 255), 1)
    # Draw a label with a name
    # below the face
    img = cv2.rectangle(img, (x1, y1 - 15), (x2 , y1), (0, 255, 255), cv2.FILLED)
    font = cv2.FONT_HERSHEY_SIMPLEX
    img = cv2.putText(img, label, (x1, y1 - 4), font, 0.5, (0, 0, 0), 2)
    return img